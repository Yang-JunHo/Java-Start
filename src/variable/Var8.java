package variable;

public class Var8 {
    public static void main(String[] args) {
        // 정수
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32,768 ~ 32,767
        int i = 2147483647; // -2,147,483,648 ~ 2,147,483,647 (약 20억)
        long l = 9223372L; // 엄청 많은 수까지 작성 가능

        // 실수
        float f = 10.0f;
        double d = 10.0;
    }
}
